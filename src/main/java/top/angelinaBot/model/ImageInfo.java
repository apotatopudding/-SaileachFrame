package top.angelinaBot.model;

import java.awt.*;

public class ImageInfo {
    private Image image;
    private int x;
    private int y;
    private int widthOfSize;
    private int heightOfSize;

    public Image getImage() { return image; }

    public void setImage(Image image) { this.image = image; }

    public int getX() { return x; }

    public void setX(int x) { this.x = x; }

    public int getY() { return y; }

    public void setY(int y) { this.y = y; }

    public int getWidthOfSize() { return widthOfSize; }

    public void setWidthOfSize(int widthOfSize) { this.widthOfSize = widthOfSize; }

    public int getHeightOfSize() { return heightOfSize; }

    public void setHeightOfSize(int heightOfSize) { this.heightOfSize = heightOfSize; }
}
